interface I18nContextValue {
  locale: Locale
  setLocale: (locale: Locale) => void
  t: (key: string, params?: Record<string, string>) => string
}

const I18nContext = createContext<I18nContextValue | null>(null)

export function I18nProvider({ children }: { children: ReactNode }) {
  // Initialize from localStorage / browser language synchronously (SSR-safe with fallback)
  const [locale, setLocaleState] = useState<Locale>(() => {
    if (typeof window === "undefined") return "it"
    try {
      const saved = localStorage.getItem("aperture-locale") as Locale | null
      if (saved && LOCALES.some((l) => l.code === saved)) return saved
      const browserLang = navigator.language.toLowerCase()
      const matched = LOCALES.find((l) => browserLang.startsWith(l.code))
      return matched ? matched.code : "it"
    } catch {
      return "it"
    }
  })

  const setLocale = (newLocale: Locale) => {
    setLocaleState(newLocale)
    try {
      localStorage.setItem("aperture-locale", newLocale)
    } catch {}
  }

  const t = (key: string, params?: Record<string, string>): string => {
    let value = messages[locale]?.[key] ?? messages.en[key] ?? key
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        value = value.replace(`{${k}}`, v)
      }
    }
    return value
  }

  return (
    <I18nContext.Provider value={{ locale, setLocale, t }}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) {
    throw new Error("useI18n must be used within I18nProvider")
  }
  return ctx
}

// Convenience hook for just the translation function
export function useT() {
  return useI18n().t
}
